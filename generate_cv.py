def strip_frontmatter(text: str) -> str:
    """Remove YAML frontmatter delimited by --- ... ---."""
    return re.sub(r"^---\s*\n.*?\n---\s*\n", "", text, count=1, flags=re.DOTALL)


def strip_myst_directives(text: str) -> str:
    """Remove MyST directives (grid, card, figure, etc.) leaving plain text."""
    # Remove ::::{grid ...} / :::{card ...} / :::: / ::: blocks
    text = re.sub(r"^:{2,4}\{[^}]*\}.*$", "", text, flags=re.MULTILINE)
    text = re.sub(r"^:{2,4}\s*$", "", text, flags=re.MULTILINE)
    # Remove ```{figure} ... ``` blocks
    text = re.sub(r"^```\{[^}]*\}.*?^```", "", text, flags=re.MULTILINE | re.DOTALL)
    # Remove :option: lines (e.g. :name:, :width:, :align:)
    text = re.sub(r"^:[a-z_]+:.*$", "", text, flags=re.MULTILINE)
    # Remove markdown links but keep text:  [text](url) -> text
    text = re.sub(r"\[([^\]]*)\]\([^)]*\)", r"\1", text)
    # Remove lines that are just a CV download link (not useful inside the CV itself)
    text = re.sub(r"^.*Descarregar o meu Curr.*$", "", text, flags=re.MULTILINE)
    # Strip emoji characters from headings (they don't render well in Typst PDF)
    text = re.sub(
        r"[\U0001F300-\U0001F9FF\U00002702-\U000027B0\U0000FE00-\U0000FE0F\U0000200D]+\s*",
        "", text
    )
    return text


def md_to_typst_items(text: str) -> str:
    """Convert Markdown bullet items and bold/italic to Typst markup."""
    lines = []
    for line in text.splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("//"):
            continue
        # Convert markdown bold **text** to Typst *text*
        stripped = re.sub(r"\*\*([^*]+)\*\*", r"*\1*", stripped)
        # Convert markdown italic *text* (single) to Typst _text_
        stripped = re.sub(r"(?<!\*)\*([^*]+)\*(?!\*)", r"_\1_", stripped)
        # Bullet items
        if stripped.startswith("- "):
            lines.append(f"  - {stripped[2:]}")
        elif stripped.startswith("### "):
            lines.append(f"\n=== {stripped[4:]}")
        elif stripped.startswith("## "):
            lines.append(f"\n== {stripped[3:]}")
        elif stripped.startswith("# "):
            continue  # skip top-level headings (we use section titles)
        else:
            lines.append(stripped)
    return "\n".join(lines)


def read_section(filepath: Path) -> str:
    """Read a markdown file and return cleaned Typst content."""
    if not filepath.exists():
        return f"// File not found: {filepath.name}\n"
    raw = filepath.read_text(encoding="utf-8")
    raw = strip_frontmatter(raw)
    raw = strip_myst_directives(raw)
    return md_to_typst_items(raw)


