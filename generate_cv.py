def gen_section(title: str, content: str) -> str:
    """Wrap content in a Typst level-1 heading section."""
    return f"\n= {title}\n\n{content}\n"


def create_cv_typ():
    """Assemble the full cv.typ from preamble + page sections."""
    parts = [gen_preamble()]

    for section_title, md_path in CV_SECTIONS:
        content = read_section(md_path)
        parts.append(gen_section(section_title, content))

    cv_typ = "\n".join(parts)

    out = Path(__file__).parent / "cv.typ"
    out.write_text(cv_typ, encoding="utf-8")
    print(f"[OK] Generated {out}  ({len(cv_typ):,} chars)")


